let users = {}, levels = [], nextId = 1;

customLevels.split('\n').reverse().forEach((row) => {
    let data = row.split('\t');
    let authors = data[1].split(', ').map((author) => {
        if (!users[author]) {
            users[author] = nextId++;
        }

        return users[author];
    });
    levels.push({
        id: levels.length + 1,
        name: data[0],
        authorIds: authors,
        baseLevel: data[2],
        releaseDate: data[3],
    });
});

users = Object.keys(users).map((user) => ({
    id: users[user],
    name: user,
}));

let jsonData = JSON.stringify({
    levels,
    users,
});

